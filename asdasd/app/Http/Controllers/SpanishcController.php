    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
      $users = SpanishC::Search($request->curso, $request->escuela, $request->status, $request->asesor, $request->date, $request->date2)->orderBy('id', 'ASC')->paginate(5);
      $users->setPath('spanish_courses?curso='.$request->curso.'&escuela='.$request->escuela.'&status='.$request->status.'&asesor='.$request->asesor.'&date='.$request->date.'&date2='.$request->date2.'&buscar=');
      return view('admin.spanish_courses.index')->with('users', $users);
    }

